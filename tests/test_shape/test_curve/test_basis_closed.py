import detroit as d3


def test_basis_closed_1():
    line = d3.line().curve(d3.curve_basis_closed)
    assert line([]) is None
    assert line([[0, 0]]) == "M0,0Z"
    assert line([[0, 0], [0, 10]]) == "M0,6.667L0,3.333Z"
    assert (
        line([[0, 0], [0, 10], [10, 10]])
        == "M1.667,8.333C3.333,10,6.667,10,6.667,8.333C6.667,6.667,3.333,3.333,1.667,3.333C0,3.333,0,6.667,1.667,8.333"
    )
    assert (
        line([[0, 0], [0, 10], [10, 10], [10, 0]])
        == "M1.667,8.333C3.333,10,6.667,10,8.333,8.333C10,6.667,10,3.333,8.333,1.667C6.667,0,3.333,0,1.667,1.667C0,3.333,0,6.667,1.667,8.333"
    )
    assert (
        line([[0, 0], [0, 10], [10, 10], [10, 0], [0, 0]])
        == "M1.667,8.333C3.333,10,6.667,10,8.333,8.333C10,6.667,10,3.333,8.333,1.667C6.667,0,3.333,0,1.667,0C0,0,0,0,0,1.667C0,3.333,0,6.667,1.667,8.333"
    )
